<p class="mt-3">Explícanos ¿qué es "Laravel Jetstream"? y ¿qué permite "Livewire" a los programadores?</p>

<p>
    Laravel Jestream , es un paquete de Laravel que nos permite tener un punto de partida bastante robusto en cuanto a
    autenticación y manejo de funcionalidades como, Recuperación de contraseñas, envío y verificación de emails, login y
    registro de usuarios. Cuenta además con two factor autenticación activables de una forma sencilla y rápida. Está
    diseñado usando Tailwind CSS y permite usar en el frontend Livewire E Inertia.Js.
</p>


<p>
    Livewire nos pemite manejar el frontend de manera reactiva, sin necesidad de acudir a frameworks del frontend como
    React, Vue.
    Es decir hacer nuestras interfaces manejables con laravel facilmente pero sin recargar las paginas completamente ya
    que su principio basico es usar peticiones Ajax
</p>
